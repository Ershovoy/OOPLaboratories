// Задача: Описать структуру время

/// <summary>
/// Структура время
/// </summary>
struct Time
{
	/// <summary>
	/// Количество часов
	/// </summary>
	short int Hours;

	/// <summary>
	/// Количество минут
	/// </summary>
	short int Minutes;

	/// <summary>
	/// Количество секунд
	/// </summary>
	short int Seconds;
};